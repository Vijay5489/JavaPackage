package javapackage;

public class AssignmentOperator {

	public static void main(String[] args) {
		
		int a= 15;
		a +=3; // means value of a + 3 so 18 is answer
		System.out.println(a);
		
		a -= 5; // don't space between two arithmetic symbol
		System.out.println(a);
		
		a *= 5;
		System.out.println(a);
		
		a /=5;
		System.out.println(a);
		 
		int c= 50;
		
		c += 5;
		System.out.println("Result =" +c); //55
		
		c /= 11;
		System.out.println("Result =" +c); //5
		
		
		
	}

}
